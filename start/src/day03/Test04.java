package day03;

public class Test04 {
	public static void main(String[] args) {
		int no1 = 5;
		int no2 = 3;
		
		System.out.println("no1 / no2 = " + (no1 / no2));	
		// 두 변수 모두 정수형이므로 나머지를 나타내지 않고 몫만 값으로 도출됨. 때문에 나머지구하기 연산자(%)가 필요하다.
		
		System.out.println("no1 + no2 = " + (no1 + no2));
		System.out.println("no1 - no2 = " + (no1 - no2));
		System.out.println("no1 * no2 = " + (no1 * no2));
		System.out.println("no1 / no2 = " + (no1 / no2));
		System.out.println("no1 % no2 = " + (no1 % no2));
	}
}
